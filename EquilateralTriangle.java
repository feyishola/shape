public class EquilateralTriangle extends Triangle{

	
		public EquilateralTriangle(String name, double A){
			super(name,A,A,A);
		}
	
			public double perimeter(){
				return super.perimeter(); 
			}
	
	
			public double area(){
				return super.area();
			}
			
			public String toString(){
					return super.toString();
				
			}
	
}